# Exercício 4: Listas Aninhadas

# a) Crie uma matriz 3x3 (lista aninhada) com números de 1 a 9.
matriz_3x3 = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# b) Acesse e imprima o elemento da segunda linha e terceira coluna.
print(matriz_3x3[1][2])  # 2ª linha e 3ª coluna

# c) Use um loop for aninhado para imprimir todos os elementos da matriz.
for elemento in matriz_3x3:
    print(elemento)